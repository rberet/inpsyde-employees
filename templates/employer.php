    <p>Employee Display Shortcode</p>
    <p><code>[employer-display]</code> display all employees in company</p>
    <p><code>[employer-display idi="121"]</code>display one employee in company with ID 121</p>
    <p><code>[employer-display idi="121, 125"]</code>display multiple employees in company with IDs 121 and 125 an so on...</p>
